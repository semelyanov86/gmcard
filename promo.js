function addSelectedCity(city) {
    const cityContainer = document.createElement('div');
    cityContainer.classList.add('selected-city');
    cityContainer.classList.add('cityStyle');
    cityContainer.dataset.city = city;

    const citySpan = document.createElement('span');
    citySpan.textContent = city;

    const deleteIcon = document.createElement('img');
    deleteIcon.classList.add('delete-icon');
    deleteIcon.classList.add('w-6');
    deleteIcon.classList.add("cursor-pointer")
    deleteIcon.src = "/assets/constructor/delete.svg"

    cityContainer.appendChild(citySpan);
    cityContainer.appendChild(deleteIcon);
    selectedCitiesContainer.appendChild(cityContainer);
}

function removeSelectedCity(city) {
    const cityContainers = selectedCitiesContainer.querySelectorAll('.selected-city');
    cityContainers.forEach(container => {
        if (container.dataset.city === city) {
            container.remove();
        }
    });
}


